      public function generateNamaLengkap($jenisKelamin = 'random') {
          if ($jenisKelamin === 'pria') {
              $namaDepan = $this->namaDepanPria[array_rand($this->namaDepanPria)];
              $namaTengah = $this->namaTengahPria[array_rand($this->namaTengahPria)];
          } elseif ($jenisKelamin === 'wanita') {
              $namaDepan = $this->namaDepanWanita[array_rand($this->namaDepanWanita)];
              $namaTengah = $this->namaTengahWanita[array_rand($this->namaTengahWanita)];
          } else {
              if (rand(0, 1) === 0) {
                  $namaDepan = $this->namaDepanPria[array_rand($this->namaDepanPria)];
                  $namaTengah = $this->namaTengahPria[array_rand($this->namaTengahPria)];
              } else {
                  $namaDepan = $this->namaDepanWanita[array_rand($this->namaDepanWanita)];
                  $namaTengah = $this->namaTengahWanita[array_rand($this->namaTengahWanita)];
              }
          }

          $namaBelakang = $this->namaBelakang[array_rand($this->namaBelakang)];
          return "$namaDepan $namaTengah $namaBelakang";
      }

